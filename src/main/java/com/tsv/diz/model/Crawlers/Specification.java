package com.tsv.diz.model.Crawlers;

public class Specification {

	String marca;
	String model;
	String pretDeLa;
	String pretPanaLa;
	String anFabrDeLa;
	String anFabrPanaLa;
	String orasParam;

	public String getMarca() {
		return marca;
	}

	public void setMarca(String query) {
		this.marca = query;
	}

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public String getPretDeLa() {
		return pretDeLa;
	}

	public void setPretDeLa(String pretDeLa) {
		this.pretDeLa = pretDeLa;
	}

	public String getPretPanaLa() {
		return pretPanaLa;
	}

	public void setPretPanaLa(String pretPanaLa) {
		this.pretPanaLa = pretPanaLa;
	}

	public String getAnFabrDeLa() {
		return anFabrDeLa;
	}

	public void setAnFabrDeLa(String anFabrDeLa) {
		this.anFabrDeLa = anFabrDeLa;
	}

	public String getAnFabrPanaLa() {
		return anFabrPanaLa;
	}

	public void setAnFabrPanaLa(String anFabrPanaLa) {
		this.anFabrPanaLa = anFabrPanaLa;
	}

	public String getOrasParam() {
		return orasParam;
	}

	public void setOrasParam(String orasParam) {
		this.orasParam = orasParam;
	}

}
